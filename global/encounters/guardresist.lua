function GuardResistSpawn(e)
	if(eq.is_content_flag_enabled("Classic_OldWorldDrops")) then
		e.self:SetFR(75);
		e.self:SetMR(75);
		e.self:SetCR(75);
		e.self:SetPR(60);
		e.self:SetDR(60);
	end
end

